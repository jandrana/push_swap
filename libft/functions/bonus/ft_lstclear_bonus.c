#include "../../libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*current;

	current = *lst;
	while (current)
	{
		*lst = current->next;
		(*del)(current->content);
		free(current);
		current = *lst;
	}
}
